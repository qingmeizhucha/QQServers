package Message;

public class SendText {
	private String send;
	private String jieshoou;
	private String text;
	private String time;
	public String getSend() {
		return send;
	}
	public void setSend(String send) {
		this.send = send;
	}
	public String getJieshoou() {
		return jieshoou;
	}
	public void setJieshoou(String jieshoou) {
		this.jieshoou = jieshoou;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
